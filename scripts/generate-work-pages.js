#!/usr/bin/env node
const fs = require("fs");
const path = require("path");
const vm = require("vm");

const root = path.resolve(__dirname, "..");
const dataPath = path.join(root, "data.js");
const generatedMarker = "generated by scripts/generate-work-pages.js";
const detailPageVersion = "20260603ai";

// 作品情報の正本は data.js。作品追加・修正後はこのスクリプトを再実行する。
function loadWorks() {
  const code = fs.readFileSync(dataPath, "utf8");
  const sandbox = {};
  vm.createContext(sandbox);
  vm.runInContext(`${code}\nthis.__works = works;`, sandbox);
  return sandbox.__works;
}

function escapeHtml(value) {
  return String(value ?? "")
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&#39;");
}

function encodeImageSrc(value) {
  return encodeURI(String(value ?? "").trim());
}

function withFallback(value) {
  const text = String(value ?? "").trim();
  return text || "—";
}

function safeWorkId(work) {
  return String(work?.id ?? "").replace(/[^a-zA-Z0-9_-]/g, "");
}

function getWorkDetailFileName(work) {
  return `work-${safeWorkId(work)}.html`;
}

function getWorkDetailHref(work) {
  return `${getWorkDetailFileName(work)}?v=${detailPageVersion}`;
}

function getPageHref(page) {
  return `${page}?v=${detailPageVersion}`;
}

function isFourPanelMangaWork(work) {
  const sub = String(work?.subcategory ?? "");
  if (sub) return /4koma|four|四コマ|4コマ/i.test(sub);
  const text = `${work?.title ?? ""} ${work?.technique ?? ""} ${work?.caption ?? ""}`;
  return /4コマ|四コマ|４コマ|四齣/i.test(text);
}

function isCopperWork(work) {
  return /エッチング|ドライポイント|アクアチント|銅版/.test(String(work?.technique ?? ""));
}

function getWorkListPagePath(work) {
  if (work.category === "digital") {
    return work.subcategory === "mini-chara" ? "digital-mini-chara.html" : "digital-illustration.html";
  }
  if (work.category === "manga") return isFourPanelMangaWork(work) ? "manga-4koma.html" : "manga-story.html";
  if (work.category === "hanga") return isCopperWork(work) ? "hanga-copper.html" : "hanga-wood.html";
  return "index.html";
}

function getWorkCategoryLabel(work) {
  const listPage = getWorkListPagePath(work);
  const labels = {
    "hanga-wood.html": { key: "category_wood", ja: "木版画" },
    "hanga-copper.html": { key: "category_copper", ja: "銅版画" },
    "digital-illustration.html": { key: "category_digital_illustration", ja: "デジタル" },
    "digital-mini-chara.html": { key: "category_digital_mini_chara", ja: "ミニキャラ" },
    "manga-4koma.html": { key: "category_manga_4koma", ja: "四コマ" },
    "manga-story.html": { key: "category_manga_story", ja: "ストーリー" },
  };
  const label = labels[listPage];
  if (!label) return "";
  return `              <p class="caption-meta caption-category-meta"><span data-i18n="cap_category">カテゴリー</span><a class="caption-category-link" href="${escapeHtml(getPageHref(listPage))}" data-i18n="${label.key}">${escapeHtml(label.ja)}</a></p>`;
}

function navClass(active) {
  return active ? " class=\"active\"" : "";
}

function dropdownTriggerClass(active) {
  return active ? " class=\"active nav-trigger\"" : " class=\"nav-trigger\"";
}

function buildHeader(work) {
  const listPage = getWorkListPagePath(work);
  const isHanga = work.category === "hanga";
  const isDigital = work.category === "digital";
  const isManga = work.category === "manga";
  return `  <button class="lang-switch" data-lang-switch type="button">EN</button>
  <header class="site-header">
    <a class="brand" href="${getPageHref("index.html")}">Kome Ume</a>
    <nav class="nav">
      <a href="${getPageHref("index.html")}" data-i18n="nav_top">TOP</a>
      <div class="nav-item nav-dropdown">
        <a${dropdownTriggerClass(isHanga)} href="${getPageHref("hanga-wood.html")}" data-i18n="nav_hanga">版画</a>
        <div class="nav-submenu">
          <a${navClass(listPage === "hanga-wood.html")} href="${getPageHref("hanga-wood.html")}" data-i18n="nav_wood">木版画</a>
          <a${navClass(listPage === "hanga-copper.html")} href="${getPageHref("hanga-copper.html")}" data-i18n="nav_copper">銅版画</a>
        </div>
      </div>
      <div class="nav-item nav-dropdown">
        <a${dropdownTriggerClass(isDigital)} href="${getPageHref("digital-illustration.html")}" data-i18n="nav_digital">デジタル</a>
        <div class="nav-submenu">
          <a${navClass(listPage === "digital-illustration.html")} href="${getPageHref("digital-illustration.html")}" data-i18n="nav_digital_illustration">イラスト</a>
          <a${navClass(listPage === "digital-mini-chara.html")} href="${getPageHref("digital-mini-chara.html")}" data-i18n="nav_digital_mini_chara">ミニキャラ</a>
        </div>
      </div>
      <div class="nav-item nav-dropdown">
        <a${dropdownTriggerClass(isManga)} href="${getPageHref("manga.html")}" data-i18n="nav_manga">漫画</a>
        <div class="nav-submenu">
          <a${navClass(listPage === "manga-4koma.html")} href="${getPageHref("manga-4koma.html")}" data-i18n="nav_manga_4koma">四コマ</a>
          <a${navClass(listPage === "manga-story.html")} href="${getPageHref("manga-story.html")}" data-i18n="nav_manga_story">ストーリー</a>
        </div>
      </div>
      <a class="shop-link" href="${getPageHref("shop.html")}" data-i18n="nav_shop">販売</a>
      <a href="${getPageHref("profile.html")}" data-i18n="nav_profile">Profile</a>
      <a class="about-link" href="${getPageHref("about.html")}" data-i18n="nav_about">About</a>
    </nav>
  </header>`;
}

function buildContact() {
  return `    <section class="site-mini-contact">
      <div><strong class="label-sns" data-i18n="label_sns">SNS</strong> <a href="https://www.instagram.com/komeume1121/" target="_blank" rel="noopener noreferrer">@komeume1121</a></div>
      <div><strong class="label-contact" data-i18n="label_contact">Contact</strong> <a href="mailto:komeume1121@gmail.com">komeume1121@gmail.com</a></div>
    </section>`;
}

function buildPager(prevWork, nextWork) {
  if (!prevWork && !nextWork) return "";
  return `      <nav class="work-detail-pager" aria-label="work navigation">
        <span class="work-detail-pager-slot work-detail-pager-prev">${prevWork ? `<a href="${escapeHtml(getWorkDetailHref(prevWork))}" data-i18n="prev_work">前の作品</a>` : ""}</span>
        <span class="work-detail-pager-slot work-detail-pager-next">${nextWork ? `<a href="${escapeHtml(getWorkDetailHref(nextWork))}" data-i18n="next_work">次の作品</a>` : ""}</span>
      </nav>`;
}

function buildDetailMedia(work, title, images) {
  const thumbnailLabel = "作品画像一覧";
  const mainAlt = images.length > 1 ? `${title} 1` : title;
  const thumbs = images.map((imagePath, index) => `          <button class="work-detail-thumb${index === 0 ? " is-active" : ""}" type="button" data-work-detail-thumb data-work-index="${index}" aria-pressed="${index === 0 ? "true" : "false"}">
            <img src="${escapeHtml(encodeImageSrc(imagePath))}" alt="${escapeHtml(`${title} ${index + 1}`)}" loading="lazy">
          </button>`).join("\n");
  return `        <div class="work-detail-media">
          <figure class="work-detail-main">
            <img class="work-detail-main-image" data-work-detail-main-image src="${escapeHtml(encodeImageSrc(images[0]))}" alt="${escapeHtml(mainAlt)}">
          </figure>
          <div class="work-detail-thumbnails" data-work-detail-thumbnails aria-label="${escapeHtml(thumbnailLabel)}">
${thumbs}
          </div>
        </div>`;
}

function buildPage(work, prevWork, nextWork) {
  const id = safeWorkId(work);
  const title = withFallback(work.title);
  const year = withFallback(work.year);
  const technique = withFallback(work.technique);
  const size = withFallback(work.size);
  const caption = withFallback(work.caption);
  const images = Array.isArray(work.images) && work.images.length ? work.images : [work.image].filter(Boolean);
  const listPage = getWorkListPagePath(work);

  return `<!doctype html>
<!-- ${generatedMarker}; do not edit directly. Update data.js and rerun: node scripts/generate-work-pages.js -->
<html lang="ja">
<head>
  <meta charset="utf-8">
  <meta name="viewport" content="width=device-width, initial-scale=1">
  <title>${escapeHtml(title)} | Kome Ume</title>
  <link rel="stylesheet" href="styles.css?v=${detailPageVersion}">
</head>
<body>
${buildHeader(work)}

  <main>
    <article class="work-detail" data-work-id="${escapeHtml(id)}">
      <header class="work-detail-header">
        <a class="work-detail-back" href="${escapeHtml(getPageHref(listPage))}" data-i18n="back_to_works">作品一覧へ</a>
      </header>
      <div class="work-detail-grid">
${buildDetailMedia(work, title, images)}
        <aside class="work-detail-caption">
          <div class="caption">
            <h2 class="caption-title">${escapeHtml(title)}</h2>
            <div class="caption-meta-list">
              <p class="caption-meta"><span data-i18n="cap_year">制作年</span><span>${escapeHtml(year)}</span></p>
              <p class="caption-meta"><span data-i18n="cap_technique">技法</span><span>${escapeHtml(technique)}</span></p>
              <p class="caption-meta"><span data-i18n="cap_size">サイズ</span><span>${escapeHtml(size)}</span></p>
${getWorkCategoryLabel(work)}
            </div>
            <p class="caption-text">${escapeHtml(caption)}</p>
          </div>
${buildPager(prevWork, nextWork)}
        </aside>
      </div>
    </article>

${buildContact()}

    <div class="back-to-top-wrap">
      <button class="button-load-more-inline back-to-top-btn" type="button" data-back-to-top data-i18n="back_to_top">ページ先頭へ</button>
    </div>
  </main>

  <footer>© Kome Ume</footer>
  <script src="data.js?v=${detailPageVersion}"></script>
  <script src="app.js?v=${detailPageVersion}"></script>
  <script src="i18n.js?v=${detailPageVersion}"></script>
</body>
</html>
`;
}

function removeOldGeneratedPages() {
  for (const file of fs.readdirSync(root)) {
    if (!/^work-[a-zA-Z0-9_-]+\.html(?:\?.*)?$/.test(file)) continue;
    const filePath = path.join(root, file);
    const content = fs.readFileSync(filePath, "utf8");
    if (content.includes(generatedMarker)) fs.unlinkSync(filePath);
  }
}

function main() {
  const works = loadWorks();
  const seen = new Set();
  for (const work of works) {
    const id = safeWorkId(work);
    if (!id) throw new Error("work id is required");
    if (seen.has(id)) throw new Error(`duplicate work id: ${id}`);
    seen.add(id);
  }

  removeOldGeneratedPages();
  works.forEach((work, index) => {
    const prevWork = index > 0 ? works[index - 1] : null;
    const nextWork = index < works.length - 1 ? works[index + 1] : null;
    fs.writeFileSync(path.join(root, getWorkDetailFileName(work)), buildPage(work, prevWork, nextWork), "utf8");
  });
  console.log(`generated ${works.length} work pages in ${root}`);
}

main();
