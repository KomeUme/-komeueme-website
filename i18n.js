function updateProfileNavDate() {
  const profileTitle = document.querySelector('summary h2[data-i18n="card_profile_title"]');
  const profileUpdatedTarget = document.querySelector("[data-profile-updated-date]");
  const siteUpdatedTarget = document.querySelector("[data-site-updated-date]");
  if (!profileTitle && !profileUpdatedTarget && !siteUpdatedTarget) return;

  let lang = "ja";
  try {
    lang = localStorage.getItem("site-lang") || "ja";
  } catch (_) {
    lang = "ja";
  }
  const dict = I18N[lang] || I18N.ja;
  const formatUpdatedLabel = (date) => {
    if (!date) return "";
    return lang === "en" ? `Updated ${date}` : `${date}更新`;
  };
  const formatDate = (value) => {
    const date = new Date(value);
    if (Number.isNaN(date.getTime())) return "";
    const year = date.getFullYear();
    const month = String(date.getMonth() + 1).padStart(2, "0");
    const day = String(date.getDate()).padStart(2, "0");
    return `${year}.${month}.${day}`;
  };

  if (profileTitle) {
    const dateEl = profileTitle.querySelector(".profile-updated-date");
    if (dateEl) dateEl.remove();
  }

  const profileUpdated = dict.profile_history_body_updated || I18N.ja.profile_history_body_updated || "";
  const profileDateLabel = formatUpdatedLabel(profileUpdated);
  if (profileUpdatedTarget) {
    profileUpdatedTarget.textContent = profileDateLabel;
    profileUpdatedTarget.hidden = !profileDateLabel;
  }

  if (siteUpdatedTarget) {
    const siteUpdated = dict.site_updated_date || I18N.ja.site_updated_date || "";
    const siteUpdatedLabel = dict.site_updated_label || I18N.ja.site_updated_label || "";
    siteUpdatedTarget.textContent = siteUpdated && siteUpdatedLabel
      ? `${siteUpdatedLabel} ${siteUpdated}`
      : "";
    siteUpdatedTarget.hidden = !siteUpdatedTarget.textContent;
  }
}

function applyLang(lang) {
  const dict = I18N[lang] || I18N.ja;
  const runLangStep = (step) => {
    try {
      step();
    } catch (error) {
      console.error("Language update step failed:", error);
    }
  };
  document.documentElement.lang = lang === "en" ? "en" : "ja";
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.dataset.i18n;
    if (dict[key]) el.textContent = dict[key];
  });
  document.querySelectorAll("[data-i18n-html]").forEach((el) => {
    const key = el.dataset.i18nHtml;
    if (dict[key]) el.innerHTML = dict[key];
  });
  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    const key = el.dataset.i18nPlaceholder;
    if (dict[key]) el.setAttribute("placeholder", dict[key]);
  });
  document.querySelectorAll(".copy-done-label").forEach((el) => {
    el.textContent = dict.copy_done || I18N.ja.copy_done;
  });
  const btn = document.querySelector("[data-lang-switch]");
  if (btn) btn.textContent = lang === "ja" ? "EN" : "JP";
  try {
    localStorage.setItem("site-lang", lang);
  } catch (_) {
    // ignore storage failures
  }
  if (typeof window.renderGallery === "function") runLangStep(window.renderGallery);
  if (typeof window.renderShopPage === "function") runLangStep(window.renderShopPage);
  if (typeof window.renderFeatureImages === "function") runLangStep(window.renderFeatureImages);
  if (typeof window.renderWorkDetailPage === "function") runLangStep(window.renderWorkDetailPage);
  if (typeof window.attachWorkDetailThumbnailControls === "function") runLangStep(window.attachWorkDetailThumbnailControls);
  if (typeof window.renderAboutPage === "function") runLangStep(window.renderAboutPage);
  runLangStep(updateProfileNavDate);
}

