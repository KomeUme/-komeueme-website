async function copyTextToClipboard(text) {
  if (navigator.clipboard && window.isSecureContext) {
    await navigator.clipboard.writeText(text);
    return;
  }
  const ta = document.createElement("textarea");
  ta.value = text;
  ta.style.position = "fixed";
  ta.style.left = "-9999px";
  document.body.appendChild(ta);
  ta.focus();
  ta.select();
  document.execCommand("copy");
  ta.remove();
}

function setupCopyEmailButtons() {
  document.querySelectorAll("[data-copy-email]").forEach((button) => {
    if (button.dataset.copyBound === "true") return;
    button.dataset.copyBound = "true";

    button.addEventListener("click", async () => {
      const email = button.dataset.copyEmail || "";
      if (!email) return;
      try {
        await copyTextToClipboard(email);
        const lang = (() => {
          try {
            return localStorage.getItem("site-lang") || "ja";
          } catch (_) {
            return "ja";
          }
        })();
        const dict = I18N[lang] || I18N.ja;
        const doneLabel = button.querySelector(".copy-done-label");
        if (doneLabel) doneLabel.textContent = dict.copy_done || I18N.ja.copy_done;
        button.classList.add("is-copied");
        button.setAttribute("aria-label", dict.copy_done || I18N.ja.copy_done);
        window.setTimeout(() => {
          button.classList.remove("is-copied");
          button.setAttribute("aria-label", "メールアドレスをコピー");
        }, 2000);
      } catch (_) {
        // no-op
      }
    });
  });
}

let translationNoticeTimer = null;

function showAutoTranslationNotice(lang) {
  let notice = document.querySelector("[data-auto-translation-notice]");
  if (lang !== "en") {
    if (notice) notice.classList.remove("is-visible");
    window.clearTimeout(translationNoticeTimer);
    return;
  }
  const dict = I18N[lang] || I18N.ja;
  if (!notice) {
    notice = document.createElement("div");
    notice.className = "auto-translation-notice";
    notice.dataset.autoTranslationNotice = "true";
    notice.setAttribute("role", "status");
    notice.setAttribute("aria-live", "polite");
    document.body.appendChild(notice);
  }
  notice.textContent = dict.auto_translation_notice || I18N.ja.auto_translation_notice;
  notice.classList.add("is-visible");
  window.clearTimeout(translationNoticeTimer);
  translationNoticeTimer = window.setTimeout(() => {
    notice.classList.remove("is-visible");
  }, 3000);
}

function initializeI18n() {
  let initial = "ja";
  try {
    initial = localStorage.getItem("site-lang") || "ja";
  } catch (_) {
    initial = "ja";
  }
  applyLang(initial);
  setupContactLinks();
  setupCopyEmailButtons();
  const btn = document.querySelector("[data-lang-switch]");
  if (btn) {
    btn.addEventListener("click", () => {
      let current = "ja";
      try {
        current = localStorage.getItem("site-lang") || "ja";
      } catch (_) {
        current = "ja";
      }
      const next = current === "ja" ? "en" : "ja";
      applyLang(next);
      showAutoTranslationNotice(next);
    });
  }
}

window.I18N = I18N;

if (document.readyState === "loading") {
  document.addEventListener("DOMContentLoaded", initializeI18n, { once: true });
} else {
  initializeI18n();
}
