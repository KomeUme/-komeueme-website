function updateProfileNavDate() {
  const profileTitle = document.querySelector('summary h2[data-i18n="card_profile_title"]');
  const profileDesc = document.querySelector('p[data-i18n="profile_desc"]');
  const introTitle = document.querySelector(".top-intro-title");
  if (!profileTitle && !profileDesc && !introTitle) return;

  let lang = "ja";
  try {
    lang = localStorage.getItem("site-lang") || "ja";
  } catch (_) {
    lang = "ja";
  }
  const dict = I18N[lang] || I18N.ja;
  const formatUpdatedLabel = (date) => {
    if (!date) return "";
    return lang === "en" ? `Updated ${date}` : `${date}更新`;
  };
  const wrapUpdatedLabel = (label) => {
    if (!label) return "";
    return lang === "en" ? ` (${label})` : `（${label}）`;
  };

  if (profileTitle) {
    const dateEl = profileTitle.querySelector(".profile-updated-date");
    if (dateEl) dateEl.remove();
  }

  const profileUpdated = dict.profile_history_body_updated || I18N.ja.profile_history_body_updated || "";
  const profileDateLabel = formatUpdatedLabel(profileUpdated);
  if (profileDesc) {
    let dateEl = profileDesc.querySelector(".profile-updated-date");
    if (!dateEl) {
      dateEl = document.createElement("span");
      dateEl.className = "profile-updated-date";
      profileDesc.appendChild(dateEl);
    }
    dateEl.textContent = wrapUpdatedLabel(profileDateLabel);
  }

  if (introTitle) {
    const introUpdated = dict.intro_block_updated || I18N.ja.intro_block_updated || "";
    const introDateLabel = formatUpdatedLabel(introUpdated);
    let dateEl = introTitle.querySelector(".profile-updated-date");
    if (!dateEl) {
      dateEl = document.createElement("span");
      dateEl.className = "profile-updated-date";
      introTitle.appendChild(dateEl);
    }
    dateEl.textContent = wrapUpdatedLabel(introDateLabel);
  }
}

function applyLang(lang) {
  const dict = I18N[lang] || I18N.ja;
  document.documentElement.lang = lang === "en" ? "en" : "ja";
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.dataset.i18n;
    if (dict[key]) el.textContent = dict[key];
  });
  document.querySelectorAll("[data-i18n-html]").forEach((el) => {
    const key = el.dataset.i18nHtml;
    if (dict[key]) el.innerHTML = dict[key];
  });
  document.querySelectorAll(".copy-done-label").forEach((el) => {
    el.textContent = dict.copy_done || I18N.ja.copy_done;
  });
  const btn = document.querySelector("[data-lang-switch]");
  if (btn) btn.textContent = lang === "ja" ? "EN" : "JP";
  try {
    localStorage.setItem("site-lang", lang);
  } catch (_) {
    // ignore storage failures
  }
  if (typeof window.renderGallery === "function") window.renderGallery();
  if (typeof window.renderFeatureImages === "function") window.renderFeatureImages();
  if (typeof window.renderWorkDetailPage === "function") window.renderWorkDetailPage();
  if (typeof window.attachWorkDetailThumbnailControls === "function") window.attachWorkDetailThumbnailControls();
  if (typeof window.renderAboutPage === "function") window.renderAboutPage();
  updateProfileNavDate();
}

function setupContactLinks() {
  const email = "komeume1121@gmail.com";
  const subject = "web-siteから。";
  const encodedSubject = encodeURIComponent(subject);
  const mailtoUrl = `mailto:${email}?subject=${encodedSubject}`;

  document.querySelectorAll('a[href^="mailto:"]').forEach((link) => {
    link.setAttribute("href", mailtoUrl);
  });
}

async function copyTextToClipboard(text) {
  if (navigator.clipboard && window.isSecureContext) {
    await navigator.clipboard.writeText(text);
    return;
  }
  const ta = document.createElement("textarea");
  ta.value = text;
  ta.style.position = "fixed";
  ta.style.left = "-9999px";
  document.body.appendChild(ta);
  ta.focus();
  ta.select();
  document.execCommand("copy");
  ta.remove();
}

function setupCopyEmailButtons() {
  document.querySelectorAll("[data-copy-email]").forEach((button) => {
    if (button.dataset.copyBound === "true") return;
    button.dataset.copyBound = "true";

    button.addEventListener("click", async () => {
      const email = button.dataset.copyEmail || "";
      if (!email) return;
      try {
        await copyTextToClipboard(email);
        const lang = (() => {
          try {
            return localStorage.getItem("site-lang") || "ja";
          } catch (_) {
            return "ja";
          }
        })();
        const dict = I18N[lang] || I18N.ja;
        const doneLabel = button.querySelector(".copy-done-label");
        if (doneLabel) doneLabel.textContent = dict.copy_done || I18N.ja.copy_done;
        button.classList.add("is-copied");
        button.setAttribute("aria-label", dict.copy_done || I18N.ja.copy_done);
        window.setTimeout(() => {
          button.classList.remove("is-copied");
          button.setAttribute("aria-label", "メールアドレスをコピー");
        }, 2000);
      } catch (_) {
        // no-op
      }
    });
  });
}

let translationNoticeTimer = null;

function showAutoTranslationNotice(lang) {
  let notice = document.querySelector("[data-auto-translation-notice]");
  if (lang !== "en") {
    if (notice) notice.classList.remove("is-visible");
    window.clearTimeout(translationNoticeTimer);
    return;
  }
  const dict = I18N[lang] || I18N.ja;
  if (!notice) {
    notice = document.createElement("div");
    notice.className = "auto-translation-notice";
    notice.dataset.autoTranslationNotice = "true";
    notice.setAttribute("role", "status");
    notice.setAttribute("aria-live", "polite");
    document.body.appendChild(notice);
  }
  notice.textContent = dict.auto_translation_notice || I18N.ja.auto_translation_notice;
  notice.classList.add("is-visible");
  window.clearTimeout(translationNoticeTimer);
  translationNoticeTimer = window.setTimeout(() => {
    notice.classList.remove("is-visible");
  }, 3000);
}

document.addEventListener("DOMContentLoaded", () => {
  let initial = "ja";
  try {
    initial = localStorage.getItem("site-lang") || "ja";
  } catch (_) {
    initial = "ja";
  }
  applyLang(initial);
  setupContactLinks();
  setupCopyEmailButtons();
  const btn = document.querySelector("[data-lang-switch]");
  if (btn) {
    btn.addEventListener("click", () => {
      let current = "ja";
      try {
        current = localStorage.getItem("site-lang") || "ja";
      } catch (_) {
        current = "ja";
      }
      const next = current === "ja" ? "en" : "ja";
      applyLang(next);
      showAutoTranslationNotice(next);
    });
  }
});

